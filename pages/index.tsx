import { Button, Input, ThemeProvider, Typography } from "@mui/material";
import type { NextPage } from "next";
import Head from "next/head";
import { useState } from "react";

import { IResponse } from "../interface/IResponse";
import { SendForm } from "../api/sendForm";
import styles from "../styles/Home.module.css";
import { Heading } from "./components/Heading";
import { SubmitMessage } from "./components/SubmitMessage";
import { theme } from "../theme";

const Home: NextPage = () => {
  const [email, setEmail] = useState<string>("");
  const [responseData, setResponseData] = useState<IResponse | null>(null);
  const [emailValid, setEmailValid] = useState<boolean>(true);

  const sendForm = async () => {
    const res = await new SendForm(email).send({ body: { email: email } });
    setResponseData(res);
  };

  const validateEmail = (email: string) => {
    const regex = /[a-z0-9]+@[a-z]+\.[a-z]{2,3}/;
    regex.test(email) ? setEmailValid(true) : setEmailValid(false);
    return regex.test(email);
  };

  const onInput = (e: React.ChangeEvent<HTMLInputElement>) => {
    setEmail((e.target as HTMLInputElement).value);
  };

  const onEnterPress = (e: React.KeyboardEvent<HTMLInputElement>) => {
    if (e.key === "Enter") {
      e.preventDefault();
      if (validateEmail(email)) {
        sendForm();
      }
    }
  };

  const onSubmit = (
    e: React.MouseEvent<HTMLButtonElement> | React.FormEvent<HTMLFormElement>
  ) => {
    e.preventDefault();
    if (validateEmail(email)) {
      sendForm();
    }
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <ThemeProvider theme={theme}>
        <main className={styles.main}>
          <Heading
            component={"h1"}
            content={"How did you like the workshops?"}
          />
          <Heading
            component={"h2"}
            content={
              "Please enter your email in the field below to participate in a short survey:"
            }
          />
          <form
            className={styles.form}
            method="POST"
            onSubmit={(e) => onSubmit(e)}
          >
            <Input
              sx={{
                color: theme.palette.secondary.dark,
                fontSize: "1.2rem",
                height: "3rem",
                width: "15rem",
              }}
              onKeyPress={(e: React.KeyboardEvent<HTMLInputElement>) =>
                onEnterPress(e)
              }
              onChange={(e: React.ChangeEvent<HTMLInputElement>) => onInput(e)}
              id="email"
              value={email}
              type="text"
              placeholder="Enter your email..."
            ></Input>
            <Typography variant="h6" component="h6">
              {emailValid ? "" : "Please enter a correct email"}
            </Typography>
            <Button
              sx={{ m: 4, py: 1, px: 4 }}
              size="large"
              onClick={(e) => onSubmit(e)}
            >
              SUBMIT
            </Button>
          </form>
          <SubmitMessage responseData={responseData} />
        </main>
      </ThemeProvider>
    </div>
  );
};

export default Home;
